package tomaynotificacionpedidos;

import java.util.ArrayList;

public class Pedido {
	
	public ArrayList<Producto> cuerpo;
	
	public Pedido(ArrayList<Producto> cuerpo) {
		this.cuerpo = cuerpo;
	}
	
	public ArrayList<Producto> getCuerpo() {
		return this.cuerpo;
	}
	
	public void setCuerpo(ArrayList<Producto> cuerpo) {
		this.cuerpo = cuerpo;
	}
	
}
